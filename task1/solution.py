def what_is_my_sign(day, month):
    SIGNS = [(22, 'Стрелец'),
             (20, 'Козирог'),
             (19, 'Водолей'),
             (21, 'Риби'),
             (21, 'Овен'),
             (21, 'Телец'),
             (21, 'Близнаци'),
             (22, 'Рак'),
             (23, 'Лъв'),
             (23, 'Дева'),
             (23, 'Везни'),
             (22, 'Скорпион')]
    return SIGNS[(month + day // SIGNS[month % 12][0]) % 12][1]
